"""
# 次元の崩壊（Dimensional Collapse）が発生していないかを確認するためのスクリプト

次元の崩壊が起きていると、「どんな文ペアを比較しても、cos類似度が常に0.8〜0.9以上の高い値になってしまう（異方性/Anisotropy）」という現象が発生します。

## 方法1: コサイン類似度の分布をみる

### やり方

ランダムな文のペア（全く関係のない文同士）を1,000ペアほど用意し、それぞれのコサイン類似度を計算して平均値や分布を見ます。

### 判定基準

* 正常: 平均値が 0.2 〜 0.5 あたりに分散しており、似ていない文同士はちゃんと低い値になる
* 次元の崩壊（危険）: 平均値が 0.8 や 0.9 を超えており、どの文を比べてもグラフが右端にびっしり固まる
    すべてのベクトルが同じ方向を向いてしまっています

## 方法2: 主成分分析（PCA）で寄与率をみる

主成分分析（PCA）を使い、埋め込みベクトルの情報が「最初の数次元」にどれだけ集中しているかを数値化します。

### やり方

自分のタスクのテキスト（例: 2,000件）をすべてベクトル化し、[データ数, ベクトルの次元数] の行列を作ります。`sklearn`の PCA を使って、累積寄与率（Explained Variance Ratio）を計算します。

### 判定基準

* 正常: 累積寄与率のグラフがなだらかに上昇する
    例えば、上位5つの主成分を合わせても、全体の情報の20〜30%程度しか説明できない（＝多くの次元に情報が分散している）
* 次元の崩壊（危険）: 最初の1〜3つの主成分だけで、全体の情報の80%や90%以上を説明できてしまう
    文字通り「特定の数次元しか機能していない」状態

## 方法3: 各次元の「分散（Variance）」を計算する

文字通り、各次元が「死んでいる（数値が全く変化しない）」かどうかを直接数え上げます。

### やり方

多数のテキストをベクトル化し、行列にします。
列方向（次元ごと）に「分散」を計算します。

### 判定基準

* 正常: ほとんどの次元で、それなりの大きさの分散（数値のばらつき）がある
* 次元の崩壊（危険）: 多くの次元で分散が 0（または限りなく0に近い極小値）になっている
    その次元は常に同じ数値しか出力しておらず、情報を表す役割を果たせていません

## その他

あるいは、Barlow Twinsのような自己教師あり学習の手法を使って、埋め込み空間を「多様な次元に情報が分散するように」学習させる方法もあります。
* Barlow Twins: https://arxiv.org/abs/2103.03230
* VICReg: https://arxiv.org/abs/2105.04906
* UNSEE: https://arxiv.org/abs/2401.15316
"""

from sklearn.decomposition import PCA
from sklearn.metrics.pairwise import cosine_similarity

# embeddings: numpy配列 [サンプル数, 次元数] (例: [1000, 768])

# 1. コサイン類似度の平均をチェック
sim_matrix = cosine_similarity(embeddings)
# 対角成分（自分自身との類似度1.0）を除外して平均を計算
avg_sim = (sim_matrix.sum() - len(embeddings)) / (sim_matrix.size - len(embeddings))
print(f"ランダムなペアの平均コサイン類似度: {avg_sim:.4f}")

# 2. PCAで上位3次元の寄与率をチェック
pca = PCA(n_components=3)
pca.fit(embeddings)
print(
    f"上位3次元だけで説明できる情報の割合: {sum(pca.explained_variance_ratio_) * 100:.2f}%"
)
